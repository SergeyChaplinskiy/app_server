const mongoose = require('mongoose');

const UserModelSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  surname: {
    type: String,
    required: true,
  },
  age: {
    type: Number,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phoneNumber: {
    type: Number,
    required: true,
  },
});

const UserModel = mongoose.model('UserModelSchema', UserModelSchema);

module.exports = { UserModel };
